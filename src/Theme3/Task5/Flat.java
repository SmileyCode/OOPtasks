package Theme3.Task5;

public class Flat {

    private int area;
    private int number;

    public Flat(int area, int number) {
        setArea(area);
        setNumber(number);
    }

    public int getArea() {
        return area;
    }

    public void setArea(int area) {
        this.area = area;
    }

    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }
}
